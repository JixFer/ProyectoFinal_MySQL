    private void MostrarHorario(){
        if(Dtm_Desde.getDate()!=null&& Dtm_Hasta.getDate()!=null){
            java.sql.Date Fecha1 = new java.sql.Date(Dtm_Desde.getDate().getTime());
            java.sql.Date Fecha2 = new java.sql.Date(Dtm_Hasta.getDate().getTime());
            try{
            PreparedStatement ps = con.prepareStatement("call Sp_BuscarHorario(?,?);");
                ps.setDate(1, Fecha1);
                ps.setDate(2, Fecha2);
                ResultSet rs=ps.executeQuery();
                DefaultTableModel modelo=new DefaultTableModel();
                modelo.addColumn("Fecha");
                modelo.addColumn("Cedula");
                modelo.addColumn("Nombre");
                modelo.addColumn("Apellido");
                modelo.addColumn("Puesto");
                modelo.addColumn("Delta");
                modelo.addColumn("Cod_Pues");
                modelo.addColumn("Actividad");
                modelo.addColumn("Jornada");
                modelo.addColumn("Obs");
                Tabla_Principal.setModel(modelo);
                String[] dato = new String[10];
                while(rs.next()){
                    dato[0]=rs.getString("Fecha");
                    dato[1]=rs.getString("Cedula");
                    dato[2]=rs.getString("Nombre1");
                    dato[3]=rs.getString("Apellido1");
                    dato[4]=rs.getString("Nombre_puesto");
                    dato[5]=rs.getString("N_delta");
                    dato[6]=rs.getString("Id_puesto");
                    dato[7]=rs.getString("Actividad");
                    dato[8]=rs.getString("Jornada");
                    dato[9]=rs.getString("Observacion");
                    modelo.addRow(dato);
                }
                //Redimensionar tabla
                Tabla_Principal.getColumnModel().getColumn(0).setPreferredWidth(120);
                Tabla_Principal.getColumnModel().getColumn(2).setPreferredWidth(80);
                Tabla_Principal.getColumnModel().getColumn(3).setPreferredWidth(80);
                Tabla_Principal.getColumnModel().getColumn(4).setPreferredWidth(80);
                Tabla_Principal.getColumnModel().getColumn(5).setPreferredWidth(30);
                Tabla_Principal.getColumnModel().getColumn(6).setPreferredWidth(30);
                Tabla_Principal.getColumnModel().getColumn(9).setPreferredWidth(30);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"ERROR al buscara : "+ex);
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Si desea ver los horarios registrados ingrese fechas para buscar");
        }
    }
    
