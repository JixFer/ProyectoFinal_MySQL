    private void Tabla_PrincipalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Tabla_PrincipalMouseClicked
        // TODO add your handling code here:
        try {
        int fila=Tabla_Principal.rowAtPoint(evt.getPoint());
        //Fecha
        SimpleDateFormat FormatoFecha= new SimpleDateFormat("yyyy-MM-dd");
        Dtm_Fecha.setDate(FormatoFecha.parse(Tabla_Principal.getValueAt(fila,0).toString()));
        Txt_Cedula.setText(Tabla_Principal.getValueAt(fila,1).toString());
        Txt_Nombre.setText(Tabla_Principal.getValueAt(fila,2).toString());
        Txt_Apellido.setText(Tabla_Principal.getValueAt(fila,3).toString());
        Txt_Puesto.setText(Tabla_Principal.getValueAt(fila,4).toString());
        Txt_Delta.setText(Tabla_Principal.getValueAt(fila,5).toString());
        Txt_CodPuesto.setText(Tabla_Principal.getValueAt(fila,6).toString());
        //Cmb_Actividad
        for(int i=0; i<=Cmb_Actividad.getItemCount()-1;i++)
        {
            if(Tabla_Principal.getValueAt(fila,7).toString().equals(Cmb_Actividad.getItemAt(i)))
            {
                Cmb_Actividad.setSelectedIndex(i);
            }
        }
        //Cmb_Jornada
        for(int i=0; i<=Cmb_Jornada.getItemCount()-1;i++)
        {
            if(Tabla_Principal.getValueAt(fila,8).toString().equals(Cmb_Jornada.getItemAt(i)))
            {
                Cmb_Jornada.setSelectedIndex(i);
            }
        }
        Txa_Obs.setText(Tabla_Principal.getValueAt(fila,9).toString());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null,"Error : "+ex);
        }
    }//GEN-LAST:event_Tabla_PrincipalMouseClicked

