    private void Btn_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_GuardarActionPerformed

        boolean obj; 
        obj=Campos_vacios();
        if (obj==false){
            java.sql.Date Fecha = new java.sql.Date(Dtm_Fecha.getDate().getTime());//Extrae el YYY-MM-dd
            try{
                PreparedStatement ps = con.prepareStatement("call Sp_VerificarHorario(?,?,?);");
                ps.setDate(1,Fecha);
                ps.setInt(2,Integer.valueOf(Txt_CodPuesto.getText()));
                ps.setString(3,Cmb_Jornada.getSelectedItem().toString());
                ResultSet rs=ps.executeQuery();
                if(rs.next()){//EXISTE EL HORARIO
                    JOptionPane.showMessageDialog(null,"Este horario ya se encuentra registrado");
                }else{//No existe el horario
                    InsertarHorario();
                    JOptionPane.showMessageDialog(null,"Horario registrado correctamente");
                    MostrarHorario();
                }   
            } catch (SQLException ex) {
                Logger.getLogger(Horario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{ 
            JOptionPane.showMessageDialog(null,"Existen campos que estan vacios");
            Txt_Cedula.requestFocus();
        }                
    }//GEN-LAST:event_Btn_GuardarActionPerformed

